// Language detection and management
class I18n {
  constructor() {
    this.currentLang = this.detectLanguage();
    this.translations = translations;
  }
  
  detectLanguage() {
    const saved = localStorage.getItem('preferredLanguage');
    if (saved && translations[saved]) {
      return saved;
    }

    const urlLang = new URLSearchParams(window.location.search).get('lang');
    if (urlLang && translations[urlLang]) {
      localStorage.setItem('preferredLanguage', urlLang);
      return urlLang;
    }

    const isMultilingualPage = document.documentElement.dataset.multilingual === 'true';

    if (!isMultilingualPage) {
      const htmlLang = document.documentElement.lang;
      if (htmlLang && translations[htmlLang]) {
        localStorage.setItem('preferredLanguage', htmlLang);
        return htmlLang;
      }
    }

    const browserLang = (navigator.language || navigator.userLanguage).slice(0, 2).toLowerCase();
    if (translations[browserLang]) {
      localStorage.setItem('preferredLanguage', browserLang);
      return browserLang;
    }

    localStorage.setItem('preferredLanguage', 'en');
    return 'en';
  }
  
  setLanguage(lang) {
    if (translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('preferredLanguage', lang);
      this.updatePage();
      this.updateURL();
      
      // Dispatch event for news feed to reload
      window.dispatchEvent(new CustomEvent('languageChanged', { 
        detail: { language: lang } 
      }));
    }
  }
  
  t(key) {
    return this.translations[this.currentLang][key] || this.translations['en'][key] || key;
  }
  
  updatePage() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      element.innerHTML = this.t(key);
    });
    
    // Update all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
      const key = element.getAttribute('data-i18n-placeholder');
      element.placeholder = this.t(key);
    });
    
    // Update all elements with data-i18n-href attribute
    document.querySelectorAll('[data-i18n-href]').forEach(element => {
      const key = element.getAttribute('data-i18n-href');
      element.href = this.t(key);
    });
    
    // Update document language
    document.documentElement.lang = this.currentLang;
    
    // Update dropdown button text to current language
    const dropdownBtnText = document.querySelector('.lang-btn-text');
    if (dropdownBtnText) {
      const langName = {
        en: 'English',
        pl: 'Polski',
        ru: 'Русский'
      };
      dropdownBtnText.textContent = langName[this.currentLang];
    }
  }
  
  updateURL() {
    // Add language to URL for SEO
    const url = new URL(window.location);
    url.searchParams.set('lang', this.currentLang);
    window.history.replaceState({}, '', url);
  }
  
  formatNumber(num) {
    return new Intl.NumberFormat(this.getLocale(), {
      minimumFractionDigits: 0,
      maximumFractionDigits: 2
    }).format(num);
  }
  
  formatCurrency(num, currency = null) {
    const currencies = {
      en: 'USD',
      pl: 'PLN',
      ru: 'RUB'
    };
    
    return new Intl.NumberFormat(this.getLocale(), {
      style: 'currency',
      currency: currency || currencies[this.currentLang] || 'EUR',
      minimumFractionDigits: 0,
      maximumFractionDigits: 0
    }).format(num);
  }
  
  getLocale() {
    const locales = {
      en: 'en-US',
      pl: 'pl-PL',
      ru: 'ru-RU'
    };
    return locales[this.currentLang] || 'en-US';
  }

  navigateTo(event, baseUrl) {
    event.preventDefault();
    const lang = localStorage.getItem('preferredLanguage') || 'en';

    const url = new URL(baseUrl, window.location.href);
    url.searchParams.set('lang', lang);

    window.location.href = url.toString();
  }
}

// Initialize i18n
const i18n = new I18n();

// Export for use in other scripts
window.i18n = i18n;

